public class Dokumentasi {

    private String judul;
    private String isi;

    public Dokumentasi(String judul, String isi) {
        this.judul = judul;
        this.isi = isi;
    }

    public String getJudul() {
        return judul;
    }

    public String getIsi() {
        return isi;
    }
}
